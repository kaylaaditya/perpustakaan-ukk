    public function apiUlasan()
    {
        $ulasan = Peminjaman::select('peminjaman.*', 'judul')
            ->join('buku', 'buku.id', 'buku_id')
            ->whereNotNull('ulasan')
            ->orWhereNotNull('rating');

        return datatables()->of($ulasan)->toJson();
    }

